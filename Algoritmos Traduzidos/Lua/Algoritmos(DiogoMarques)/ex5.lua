--EXERCICIO5: Com os lados a, b , c de um paralelepípedo.
--            Calcular e imprimir a diagonal.

io.write'Digite a base:'

a=io.read()

io.write'Digite a altura:'

b=io.read()

io.write'Digite a profundidade:'

c=io.read()

diagonal=math.sqrt((a*2)+(b*2)+(c*2))

print ' A diagonal é: ' io.write(diagonal)


